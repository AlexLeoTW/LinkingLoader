package click.alexleo.linkingloader.utils.record;

import static org.junit.Assert.*;

import org.junit.Test;

public class TextRecordTest {

    @Test
    public void testEquals() {
	TextRecord expect = new TextRecord(0, 29, new byte[]{
		    0x17, 0x20, 0x2D,
		    0x69, 0x20, 0x2D,
		    0x4B, 0x10, 0x10, 0x36,
		    0x03, 0x20, 0x26,
		    0x29, 0x00, 0x00,
		    0x33, 0x20, 0x07,
		    0x4B, 0x10, 0x10, 0x5D,
		    0x3F, 0x2F, (byte) 0xEC,
		    0x03, 0x20, 0x10
		});
	TextRecord actual = new TextRecord(0, 29, new byte[]{
		    0x17, 0x20, 0x2D,
		    0x69, 0x20, 0x2D,
		    0x4B, 0x10, 0x10, 0x36,
		    0x03, 0x20, 0x26,
		    0x29, 0x00, 0x00,
		    0x33, 0x20, 0x07,
		    0x4B, 0x10, 0x10, 0x5D,
		    0x3F, 0x2F, (byte) 0xEC,
		    0x03, 0x20, 0x10
		});
	assertEquals(true, expect.equals(actual));
    }
    
    @Test
    public void testNew() throws Exception {
	TextRecord expect = new TextRecord(0, 29, new byte[]{
	    0x17, 0x20, 0x2D,
	    0x69, 0x20, 0x2D,
	    0x4B, 0x10, 0x10, 0x36,
	    0x03, 0x20, 0x26,
	    0x29, 0x00, 0x00,
	    0x33, 0x20, 0x07,
	    0x4B, 0x10, 0x10, 0x5D,
	    0x3F, 0x2F, (byte) 0xEC,
	    0x03, 0x20, 0x10
	});
	TextRecord actual = new TextRecord("T0000001D17202D69202D4B1010360320262900003320074B10105D3F2FEC032010");
	System.out.println(expect);
	System.out.println(actual);
	assertEquals(expect, actual);
    }

}
